const overlay = document.getElementById('langOverlay');
const langButtons = document.querySelectorAll('[data-lang-choice]');
const changeLanguageButton = document.getElementById('changeLanguage');
const priorityGrid = document.getElementById('priorityGrid');
const linksGrid = document.getElementById('linksGrid');

function applyLanguage(lang) {
  const selected = content[lang] || content.en;
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.getAttribute('data-i18n');
    if (selected[key]) node.textContent = selected[key];
  });

  renderCards(lang);
}

function cardTemplate(item, lang, isPriority = false) {
  return `
    <a class="tile ${isPriority ? 'priority' : ''}" href="${item.href}" target="_blank" rel="noopener noreferrer">
      <div class="tile-head">
        <div class="tile-icon"><i class="bi ${item.icon}"></i></div>
        <span class="tile-tag">${item.tag[lang]}</span>
      </div>
      <h3>${item.title[lang]}</h3>
      <p>${item.desc[lang]}</p>
      <span class="tile-arrow">↗</span>
    </a>
  `;
}

function renderCards(lang) {
  priorityGrid.innerHTML = linkData.priority.map((item) => cardTemplate(item, lang, true)).join('');
  linksGrid.innerHTML = linkData.all.map((item) => cardTemplate(item, lang, false)).join('');
}

function openLanguageOverlay() {
  overlay.classList.remove('hidden');
}

function closeLanguageOverlay() {
  overlay.classList.add('hidden');
}

langButtons.forEach((button) => {
  button.addEventListener('click', () => {
    const lang = button.getAttribute('data-lang-choice');
    applyLanguage(lang);
    closeLanguageOverlay();
  });
});

changeLanguageButton.addEventListener('click', openLanguageOverlay);

applyLanguage('en');
openLanguageOverlay();
